package pe.marista.sigma.bean.reporte;

import java.io.Serializable;

/**
 *
 * @author MS002
 */
public class EnvioAlumno implements Serializable{

    private String idEstudiante;
    private String nombre;
    private String apellidoPat;
    private String apellidoMat;
    private String nombreGraAcaHab;
    private String seccion;
    private String asunto;
    private String mensaje;

    public String getIdEstudiante() {
        return idEstudiante;
    }

    public void setIdEstudiante(String idEstudiante) {
        this.idEstudiante = idEstudiante;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellidoPat() {
        return apellidoPat;
    }

    public void setApellidoPat(String apellidoPat) {
        this.apellidoPat = apellidoPat;
    }

    public String getApellidoMat() {
        return apellidoMat;
    }

    public void setApellidoMat(String apellidoMat) {
        this.apellidoMat = apellidoMat;
    }

    public String getNombreGraAcaHab() {
        return nombreGraAcaHab;
    }

    public void setNombreGraAcaHab(String nombreGraAcaHab) {
        this.nombreGraAcaHab = nombreGraAcaHab;
    }

    public String getSeccion() {
        return seccion;
    }

    public void setSeccion(String seccion) {
        this.seccion = seccion;
    }

    public String getAsunto() {
        return asunto;
    }

    public void setAsunto(String asunto) {
        this.asunto = asunto;
    }

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }
    
    
    
    
}
