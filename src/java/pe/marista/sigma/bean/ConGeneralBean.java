public class ConGeneralBean {
    
    private String nombre;
    private Float uno;
    private Float dos;
    private Float tres;
    private Float cuatro;
    private Float cinco;
    private Float seis;
    private Float noSatisfecho;
    private Float medSatisfecho;
    private Float satisfecho;      

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Float getUno() {
        return uno;
    }

    public void setUno(Float uno) {
        this.uno = uno;
    }

    public Float getDos() {
        return dos;
    }

    public void setDos(Float dos) {
        this.dos = dos;
    }

    public Float getTres() {
        return tres;
    }

    public void setTres(Float tres) {
        this.tres = tres;
    }

    public Float getCuatro() {
        return cuatro;
    }

    public void setCuatro(Float cuatro) {
        this.cuatro = cuatro;
    }

    public Float getCinco() {
        return cinco;
    }

    public void setCinco(Float cinco) {
        this.cinco = cinco;
    }

    public Float getSeis() {
        return seis;
    }

    public void setSeis(Float seis) {
        this.seis = seis;
    }

    public Float getNoSatisfecho() {
        return noSatisfecho;
    }

    public void setNoSatisfecho(Float noSatisfecho) {
        this.noSatisfecho = noSatisfecho;
    }

    public Float getMedSatisfecho() {
        return medSatisfecho;
    }

    public void setMedSatisfecho(Float medSatisfecho) {
        this.medSatisfecho = medSatisfecho;
    }

    public Float getSatisfecho() {
        return satisfecho;
    }

    public void setSatisfecho(Float satisfecho) {
        this.satisfecho = satisfecho;
    }
    
    
    
}
